/*Программа должна выводить таблицу квадратов чисел от 1 до 5.
В первом столбце должны находиться числа от 1 до 5 во втором столбце — квадраты этих чисел.*/

#include <iostream>

int main()
{
    setlocale(LC_ALL, "RU");

    std::cout << "Число\t\t" << "Квадрат числа" << "\n";
    std::cout << "------\t\t" << "--------------" << "\n\n";

    for (int i = 1; i <= 5; ++i)
    {
        std::cout << i << "\t\t" << (i * i) << "\n";
    }
}


